#include<bits/stdc++.h>
using namespace std;

int main() {
    int X, Z;
    cin >> X >> Z;
    
    cout << X << " + " << Z << " = " << (long long)(X + Z) << endl;
    cout << X << " * " << Z << " = " << (long long)(X * Z) << endl;
    cout << X << " - " << Z << " = " << X - Z << endl;
    
    return 0;
}